int		ft_iterative_factorial(int nb)
{
	int n;

	if (nb == 0)
		return (1);
	if (nb > 12 || nb < 0)
		return (0);
	n = nb - 1;
	while (n != 0)
	{
		nb = nb * n;
		n--;
	}
	return (nb);
}
