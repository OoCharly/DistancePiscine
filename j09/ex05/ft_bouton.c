int		ft_bouton(int i, int j, int k)
{
	if ((i < j && j < k) || (i > j && j > k))
		return (j);
	if ((k < i && i < j) || (k > i && i > j))
		return (i);
	if ((i < k && k < j) || (i > k && k > j))
		return (k);
}
