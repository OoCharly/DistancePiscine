void	ft_code(char *c, int i, char ch)
{
	c[i] = ((c[i] + 10 - ch) % 26) + ch;
}

char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= 90 && str[i] >= 65)
			ft_code(str, i, 'A');
		if (str[i] <= 122 && str[i] >= 97)
			ft_code(str, i, 'a');
		i++;
	}
	return (str);
}
