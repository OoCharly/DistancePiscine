#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	while (*(t_byte*)s1 == *(t_byte*)s2 && *(t_byte*)s1 && *(t_byte*)s2)
	{
		s1++;
		s2++;
	}
	return ((int)(*(t_byte*)s1 - *(t_byte*)s2));
}
